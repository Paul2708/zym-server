package de.paul2708.claim.model.identifier;

/**
 * This class holds the id for models.
 *
 * @author Paul2708
 */
public class Identifier {

    private int id;

    /**
     * Set the id.
     *
     * @param id new id
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * Get the id.
     *
     * @return id
     */
    public int getId() {
        return id;
    }
}
